#include "libft.h"

int		ft_isnum(char *str)
{
	if (!str)
		return (0);
	while (*str)
	{
		if ((!(ft_isdigit(*str))) && !(*str == '-' && (ft_isdigit(*(str + 1)))))
			return (0);
		str++;
	}
	return (1);
}
