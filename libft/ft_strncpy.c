#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*tmp;

	tmp = s1;
	ft_memset(s1, '\0', n);
	while (n-- && (*tmp++ = *s2++))
		;
	return (s1);
}
