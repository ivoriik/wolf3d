#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *str;

	if (n == 0)
		return ;
	str = (unsigned char *)s;
	while (n != 0)
	{
		*str++ = 0;
		n--;
	}
}
